void main() {
  num angka1 = 10;
  num angka2 = 12;

  // operator sama dengan(==)
  print('== :');
  print(angka1 == angka2);

  // operator tidak sama dengan !=
  print('!= :');
  print(angka1 != angka2);

  // operator lebih besar dari >
  print('> :');
  print(angka1 > angka2);


  // operator lebih kurang dari <
  print('< :');
  print(angka1 < angka2);

  // operator lebih dari atau sama dengan >=
  print('>= :');
  print(angka1 >= angka2);

  // operator kurang dari atau sama dengan <=
  print('<= :');
  print(angka1 <= angka2);
}
